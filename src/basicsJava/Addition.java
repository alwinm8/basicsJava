package basicsJava;

public class Addition {
	/*
	 * This is my millionth java project!
	 */

	public static void main(String[] args) {
		int x = 10;
		int y = 20;
		int result = 0;
		
		//Addition operation and result
		result = x + y;
		System.out.println("The result of the addition is " + result + ".");
		
		//Subtraction operation and result
		result = y - x;
		System.out.println("The result of the subtraction is " + result + ".");
		
		//Multiplication operation and result
		result = x * y;
		System.out.println("The result of the multiplication is " + result + ".");
		
		//Division operation and result
		result = y / x;
		System.out.println("The result of the division is " + result + ".");
		
		

	}

}
